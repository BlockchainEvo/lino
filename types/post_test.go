package types

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestPostID(t *testing.T) {
	assert.Equal(
		t, PostID([]byte("Address"), 1),
		    []byte{0x41, 0x64, 0x64, 0x72, 0x65, 0x73, 0x73, 0x1, 0x1})

}

func TestPostKey(t *testing.T) {
	assert.Equal(
		t, PostKey(PostID([]byte("Address"), 1)),
		    []byte{0x70, 0x6f, 0x73, 0x74, 0x2f, 0x41, 0x64, 0x64, 0x72, 0x65, 0x73, 0x73, 0x1, 0x1})
}